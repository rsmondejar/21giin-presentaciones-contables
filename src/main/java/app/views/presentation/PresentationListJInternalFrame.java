    /**
     * Load Data.
     */
    public void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTablePresentations.getModel();

        List<Presentation> items = PresentationController.list();

        if (items != null) {
            // Add data
            Object[] rowData = new Object[model.getColumnCount()];

            for (int i = 0; i < items.size(); i++) {
                rowData[0] = items.get(i).getId();
                rowData[1] = items.get(i).getConvocatory().getName();
                rowData[2] = items.get(i).getUser().getLogin();
                rowData[3] = items.get(i).getDate().toString();
                rowData[4] = items.get(i).getIsValid();

                model.addRow(rowData);
            }
        }
    }

