    /**
     * Load Data.
     */
    public void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTableUsers.getModel();

        // @TODO: Load Data
        List<User> items = UserController.list();

        // @TODO: Add data
        Object[] rowData = new Object[model.getColumnCount()];
        for (int i = 0; i < items.size(); i++) {
            rowData[0] = items.get(i).getId();
            rowData[1] = items.get(i).getLogin();
            rowData[2] = items.get(i).getRoleName();
            rowData[3] = items.get(i).getMunicipalityName();

            model.addRow(rowData);
        }
    }

