    private void jTableConvocatoriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConvocatoriesMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int id = Integer.parseInt(source.getModel().getValueAt(row, 0).toString());
        
        Convocatory convocatory = ConvocatoryController.findById(id);

        ConvocatoryShowJInternalFrame convocatoryShow = new ConvocatoryShowJInternalFrame(jDesktopPanelContainer);
        jDesktopPanelContainer.add(convocatoryShow);
        convocatoryShow.loadData(convocatory);
        
        this.hide();
        
        convocatoryShow.setVisible(true);
    }//GEN-LAST:event_jTableConvocatoriesMouseClicked


