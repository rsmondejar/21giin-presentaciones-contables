    private void jTablePresentationsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePresentationsMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int id = Integer.parseInt(source.getModel().getValueAt(row, 0).toString());

        Presentation presentation = PresentationController.findById(id);

        PresentationShowJInternalFrame presentationShow = new PresentationShowJInternalFrame(jDesktopPanelContainer);
        jDesktopPanelContainer.add(presentationShow);
        presentationShow.loadData(presentation);

        this.hide();

        presentationShow.setVisible(true);
    }//GEN-LAST:event_jTablePresentationsMouseClicked


