    private void jTableUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsersMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int id = Integer.parseInt(source.getModel().getValueAt(row, 0).toString());

        User user = UserController.findById(id);

        UserShowJInternalFrame userShow = new UserShowJInternalFrame(jDesktopPanelContainer);
        jDesktopPanelContainer.add(userShow);
        userShow.loadData(user);

        this.hide();

        userShow.setVisible(true);
    }//GEN-LAST:event_jTableUsersMouseClicked


