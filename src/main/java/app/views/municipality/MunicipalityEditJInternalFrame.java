    private void jButtonMunicipioUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMunicipioUpdateActionPerformed
        try {

            int id = Integer.parseInt(jTextFieldMunicipioId.getText());
            Municipality municipality = MunicipalityController.findById(id);

            municipality.setName(getInputName());
            municipality.setCategoryId(Integer.parseInt(getInputCategory()));

            MunicipalityController.update(id, municipality);
            
            Dialog.info("Municipio actualizado correctamente", "Info");
        } catch (NumberFormatException e) {
            Log.error(e);
            Dialog.error(e.getMessage(), "Error actualizando municipio");
        }
    }//GEN-LAST:event_jButtonMunicipioUpdateActionPerformed

    /**
     * Validate Inputs.
     *
     * @return True if inputs are valid
     */
    private Boolean validateInputs() {
        return validateInputName() && validateInputCategory();
    }

    /**
     * Validate Input Name.
     *
     * @return
     */
    private Boolean validateInputName() {
        String name = getInputName();
        Boolean isValid = name.length() > 0 && name.length() <= 30;

        if (isValid || name.length() == 0) {
            jTextFieldMunicipioName.setBackground(Color.white);
        } else {
            jTextFieldMunicipioName.setBackground(Color.orange);
        }

        return isValid;
    }

    /**
     * Validate Input Category.
     *
     * @return
     */
    private Boolean validateInputCategory() {
        // TODO: Validation
        //String category = getInputCategory();
        
        Boolean isValid = true;

        if (isValid) {
            jComboBoxMunicipioCategory.setBackground(Color.white);
        } else {
            jComboBoxMunicipioCategory.setBackground(Color.orange);
        }

        return isValid;
    }

    /**
     * Get Input Name.
     *
     * @return Input Name
     */
    private String getInputName() {
        return jTextFieldMunicipioName.getText();
    }

    /**
     * Get Input Category.
     *
     * @return Input Category.
     */
    private String getInputCategory() {
        return jComboBoxMunicipioCategory.getSelectedItem().toString();
    }
    
