    /**
     * Load Data.
     */
    public void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTableConvocatories.getModel();
        
        // @TODO: Load Data
        List<Convocatory> items = ConvocatoryController.list();
        
        // @TODO: Add data
        Object rowData[] = new Object[model.getColumnCount()];
                
        for (int i = 0; i < items.size(); i++) {
            rowData[0] = items.get(i).getId();
            rowData[1] = items.get(i).getName();
            rowData[2] = items.get(i).getStartDate().toString();
            rowData[3] = items.get(i).getEndDate().toString();
            rowData[4] = items.get(i).getIsValid();
            
            model.addRow(rowData);
        }
    }
    
